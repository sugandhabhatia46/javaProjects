package com.in28minutes.ifStatement.examples;

public class IfStatementRunner {

	public static void main(String[] args) {
		int i = 28;
		if (i == 25) {
			System.out.println("i is 25");
		} else if (i == 24) {
			System.out.println("i = 24");
		} else {
			System.out.println("i is neither 25 or 24");
		}
	}

	}


